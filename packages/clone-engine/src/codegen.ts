function modelSwift(b: AppBlueprint): string {
  const arrays = b.tabs
    .map((tab, i) => {
      const items = tab.items
        .map(
          (it) =>
            `        Entry(title: "${sw(it.title)}", subtitle: "${sw(it.subtitle)}", detail: "${sw(it.detail)}")`,
        )
        .join(",\n");
      return `    static let tab${i}: [Entry] = [\n${items}\n    ]`;
    })
    .join("\n\n");
  return `import Foundation

// The single content model the cloned ${sw(b.primaryEntity)} app revolves around.
struct Entry: Identifiable, Hashable {
    let id = UUID()
    let title: String
    let subtitle: String
    let detail: String

    /// "8/12 days" or "7 of 10" -> 0.58; nil when the detail isn't a ratio.
    var progress: Double? {
        let parts = detail
            .replacingOccurrences(of: " of ", with: "/")
            .split(separator: "/")
        guard parts.count == 2 else { return nil }
        let n = Double(parts[0].filter { $0.isNumber || $0 == "." })
        let d = Double(parts[1].filter { $0.isNumber || $0 == "." })
        guard let n, let d, d > 0, n <= d else { return nil }
        return n / d
    }
}

enum SampleData {
${arrays}
}
`;
}

