function xcodeReadme(b: AppBlueprint, projectName: string): string {
  return `# ${b.appName}

> ${b.tagline}

A native SwiftUI app generated by the **Kittie Builder**. Core entity:
**${b.primaryEntity}** · accent \`${b.accentHex}\` · bundle id \`${b.bundleId}\`.

## Screens
${b.tabs.map((t) => `- **${t.title}** (${t.kind}) — ${t.headline}`).join("\n")}

## Build & run

\`\`\`sh
open ${projectName}.xcodeproj
# or headless:
xcodebuild -project ${projectName}.xcodeproj -scheme ${projectName} \\
  -destination 'generic/platform=iOS Simulator' build CODE_SIGNING_ALLOWED=NO
\`\`\`
`;
}

/** Render a validated blueprint into a ready-to-open Xcode project (no xcodegen). */
export function generateXcodeProject(b: AppBlueprint): { projectName: string; files: GeneratedFile[] } {
  const projectName = projectNameFor(b);
  const typeNames = b.tabs.map((t, i) => tabTypeName(t, i));
  const files: GeneratedFile[] = [
    { path: `${projectName}.xcodeproj/project.pbxproj`, contents: pbxproj(projectName, b.bundleId) },
    { path: "README.md", contents: xcodeReadme(b, projectName) },
    { path: `${projectName}/App.swift`, contents: appSwift(b, projectName) },
    { path: `${projectName}/Theme.swift`, contents: themeSwift(b) },
    { path: `${projectName}/Components.swift`, contents: componentsSwift() },
    { path: `${projectName}/Models.swift`, contents: modelSwift(b) },
    { path: `${projectName}/RootView.swift`, contents: rootViewSwift(b, typeNames) },
    ...b.tabs.map((tab, i) => {
      const tn = typeNames[i] ?? tabTypeName(tab, i);
      return { path: `${projectName}/${tn}.swift`, contents: screenSwift(tab, i, tn, b.primaryEntity) };
    }),
  ];
  return { projectName, files };
}
