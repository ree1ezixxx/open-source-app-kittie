function screenBody(tab: BlueprintTab, entity: string): string {
  const sym = tab.symbol;
  const link = (inner: string) =>
    `NavigationLink { EntryDetailView(entry: entry, entityName: "${sw(entity)}", symbol: "${sym}") } label: { ${inner} }`;
  switch (tab.kind) {
    case "feed":
      return `        ScrollView {
            ScreenHeader(headline: "${sw(tab.headline)}", subhead: "${sw(tab.subhead)}")
            LazyVStack(alignment: .leading, spacing: 12) {
                if let first = items.first {
                    NavigationLink {
                        EntryDetailView(entry: first, entityName: "${sw(entity)}", symbol: "${sym}")
                    } label: {
                        EntryCard(entry: first, symbol: "${sym}")
                    }
                    .buttonStyle(.plain)
                }
                if items.count > 1 {
                    Text("UP NEXT")
                        .font(.caption2.weight(.bold))
                        .foregroundStyle(.secondary)
                        .padding(.top, 6)
                    VStack(spacing: 0) {
                        ForEach(items.dropFirst()) { entry in
                            ${link(`EntryRow(entry: entry, symbol: "${sym}")`)}
                            .buttonStyle(.plain)
                            .padding(.vertical, 6)
                            if entry.id != items.last?.id { Divider() }
                        }
                    }
                    .padding(.horizontal, 14)
                    .padding(.vertical, 4)
                    .background(Theme.surface)
                    .clipShape(RoundedRectangle(cornerRadius: 16))
                }
            }
            .padding()
        }`;
    case "grid":
      return `        ScrollView {
            ScreenHeader(headline: "${sw(tab.headline)}", subhead: "${sw(tab.subhead)}")
            LazyVGrid(columns: [GridItem(.flexible(), spacing: 11), GridItem(.flexible(), spacing: 11)], spacing: 11) {
                ForEach(Array(items.enumerated()), id: \\.element.id) { index, entry in
                    NavigationLink {
                        EntryDetailView(entry: entry, entityName: "${sw(entity)}", symbol: "${sym}")
                    } label: {
                        EntryTile(entry: entry, symbol: "${sym}", filled: index == 0)
                    }
                    .buttonStyle(.plain)
                }
            }
            .padding()
        }`;
    case "form":
      return `        Form {
            Section {
                TextField("Title", text: $draftTitle)
                TextField("Notes", text: $draftNotes, axis: .vertical)
                    .lineLimit(3, reservesSpace: true)
            }
            Section {
                Button {
                    // demo action
                } label: {
                    Text("Add").font(.headline).frame(maxWidth: .infinity)
                }
                .buttonStyle(.borderedProminent)
                .tint(Theme.accent)
                .listRowInsets(EdgeInsets())
                .listRowBackground(Color.clear)
            }
            Section("Recent") {
                ForEach(items) { entry in
                    ${link(`EntryRow(entry: entry, symbol: "${sym}")`)}
                }
            }
        }`;
    case "profile":
      return `        List {
            Section {
                VStack(spacing: 6) {
                    Circle()
                        .fill(LinearGradient(colors: [Theme.accent, Theme.accent.opacity(0.55)],
                                             startPoint: .topLeading, endPoint: .bottomTrailing))
                        .frame(width: 84, height: 84)
                        .overlay(Image(systemName: "person.fill").font(.largeTitle).foregroundStyle(.white))
                    Text("${sw(tab.headline)}").font(.title3.bold())
                    Text("${sw(tab.subhead)}").font(.footnote).foregroundStyle(.secondary)
                }
                .frame(maxWidth: .infinity)
                .listRowBackground(Color.clear)
            }
            Section {
                ForEach(items) { entry in
                    ${link(`EntryRow(entry: entry, symbol: "${sym}")`)}
                }
            }
        }`;
    default: // list
      return `        List {
            Section {
                ForEach(items) { entry in
                    ${link(`EntryRow(entry: entry, symbol: "${sym}")`)}
                }
            } header: {
                if !"${sw(tab.subhead)}".isEmpty {
                    Text("${sw(tab.subhead)}").textCase(nil).font(.footnote)
                }
            }
        }
            .listStyle(.insetGrouped)`;
  }
}

function screenSwift(tab: BlueprintTab, i: number, typeName: string, entity: string): string {
  const needsFormState = tab.kind === "form";
  const state = needsFormState
    ? `    @State private var draftTitle = ""\n    @State private var draftNotes = ""\n`
    : "";
  const titleMode = tab.kind === "list" || tab.kind === "form" || tab.kind === "profile" ? "large" : "inline";
  return `import SwiftUI

struct ${typeName}: View {
    private let items = SampleData.tab${i}
${state}
    var body: some View {
        NavigationStack {
${screenBody(tab, entity)}
                .navigationTitle("${sw(tab.title)}")
                .navigationBarTitleDisplayMode(.${titleMode})
        }
    }
}
`;
}

function rootViewSwift(b: AppBlueprint, typeNames: string[]): string {
  const tabs = b.tabs
    .map(
      (tab, i) => `            ${typeNames[i]}()
                .tabItem { Label("${sw(tab.title)}", systemImage: "${tab.symbol}") }`,
    )
    .join("\n");
  return `import SwiftUI

struct RootView: View {
    var body: some View {
        TabView {
${tabs}
        }
        .tint(Theme.accent)
    }
}
`;
}

