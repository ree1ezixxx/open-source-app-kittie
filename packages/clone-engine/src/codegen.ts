/** Render a validated blueprint into a complete xcodegen project. */
export function generateProject(b: AppBlueprint): { projectName: string; files: GeneratedFile[] } {
  const projectName = projectNameFor(b);
  const typeNames = b.tabs.map((t, i) => tabTypeName(t, i));
  const files: GeneratedFile[] = [
    { path: "project.yml", contents: projectYml(b, projectName) },
    { path: "README.md", contents: readme(b, projectName) },
    { path: "Sources/App.swift", contents: appSwift(b, projectName) },
    { path: "Sources/Theme.swift", contents: themeSwift(b) },
    { path: "Sources/Components.swift", contents: componentsSwift() },
    { path: "Sources/Models.swift", contents: modelSwift(b) },
    { path: "Sources/RootView.swift", contents: rootViewSwift(b, typeNames) },
    ...b.tabs.map((tab, i) => {
      const tn = typeNames[i] ?? tabTypeName(tab, i);
      return { path: `Sources/${tn}.swift`, contents: screenSwift(tab, i, tn, b.primaryEntity) };
    }),
  ];
  return { projectName, files };
}

