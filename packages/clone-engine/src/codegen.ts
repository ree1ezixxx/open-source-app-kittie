import type { AppBlueprint, BlueprintTab, GeneratedFile } from "./types.js";

/* ============================================================
   Stage 2 — deterministic SwiftUI codegen.

   Given a validated blueprint, emit a complete xcodegen project. Everything
   here is pure string templating over a fixed, iOS-17-safe SwiftUI vocabulary,
   so the output ALWAYS compiles. The model's content only flows in as escaped
   string literals and a clamped enum (TabKind / SF symbol).
   ============================================================ */

/** Escape a JS string for safe embedding inside a Swift "..." literal. */
function sw(s: string): string {
  return s
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\r?\n/g, " ")
    .replace(/\t/g, " ");
}

/** PascalCase, ASCII-letters-only Swift identifier; always starts with a letter. */
function pascal(s: string, fallback = "Clone"): string {
  const cleaned = (s.match(/[A-Za-z0-9]+/g) ?? [])
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("");
  const id = cleaned.replace(/^[0-9]+/, "");
  return id || fallback;
}

/** Project / app type prefix derived from the brand name. */
export function projectNameFor(b: AppBlueprint): string {
  return pascal(b.appName, "CloneApp");
}

/** Unique Swift type name per tab (deduped by index). */
function tabTypeName(tab: BlueprintTab, i: number): string {
  return `${pascal(tab.title, `Screen${i + 1}`)}Screen${i}`;
}

/** "#RRGGBB" -> 0xRRGGBB literal. */
function hexLiteral(hex: string): string {
  return `0x${hex.replace("#", "").toUpperCase()}`;
}

